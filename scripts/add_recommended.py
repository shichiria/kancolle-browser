# Helper: ship type count condition
def ST(name, stypes, value):
    return {"type": "ShipTypeCount", "ship_type": name, "stypes": stypes, "value": value}

# Helper: flagship type condition
def FS(name, stypes):
    return {"type": "FlagshipType", "ship_type": name, "stypes": stypes}

# Helper: contains ship name (all required)
def SN(names, count):
    return {"type": "ContainsShipName", "names": names, "count": count}

# Helper: contains ship name any (OR match)
def SNA(names, count):
    return {"type": "ContainsShipNameAny", "names": names, "count": count}

# Helper: ship count
def SC(value):
    return {"type": "ShipCount", "value": value}

# Helper: only ship types
def ONLY(desc, stypes):
    return {"type": "OnlyShipTypes", "desc": desc, "stypes": stypes}

# Helper: per-map recommendation
def M(area, *fleet):
    return {"area": area, "fleet": list(fleet)}

