def main():
    with open(JSON_PATH, "r", encoding="utf-8") as f:
        quests = json.load(f)

    updated_count = 0
    for quest in quests:
        qid = quest["quest_id"]
        if qid not in UPDATES:
            # Preserve existing recommended data (e.g. from other agents)
            if "recommended" not in quest:
                quest["recommended"] = []
            continue

        update = UPDATES[qid]

        if "conditions" in update:
            quest["conditions"] = update["conditions"]

        if "recommended" in update:
            quest["recommended"] = update["recommended"]

        for key in ("area", "rank", "boss_only", "count"):
            if key in update:
                quest[key] = update[key]

        updated_count += 1

    # Ensure all quests have recommended field (empty list)
    for quest in quests:
        if "recommended" not in quest:
            quest["recommended"] = []

    with open(JSON_PATH, "w", encoding="utf-8") as f:
        json.dump(quests, f, ensure_ascii=False, indent=2)
        f.write("\n")

    print(f"Updated {updated_count} quests")
    with_rec = sum(1 for q in quests if q.get("recommended"))
    total_maps = sum(len(q["recommended"]) for q in quests if q.get("recommended"))
    print(f"With recommended: {with_rec} quests, {total_maps} map entries")


if __name__ == "__main__":
    main()
